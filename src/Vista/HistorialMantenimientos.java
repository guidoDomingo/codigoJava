     private void formatoTabla() {
        String[] cabeceras = {"Id", "Tipo Mantenimiento", "Equipo", "Comentario", "Usuario","Nombres","Fecha"};

        DefaultTableModel m = new DefaultTableModel(cabeceras, 0) {
            @Override
            public Class<?> getColumnClass(int column) {
                if (getRowCount() > 0) {
                    Object value = getValueAt(0, column);
                    if (value != null) {
                        return getValueAt(0, column).getClass();
                    }
                }

                return super.getColumnClass(column);
            }

            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        tabla.setModel(m);
        tabla.setCellSelectionEnabled(false);
        tabla.setRowSelectionAllowed(true);
        tabla.setFillsViewportHeight(true);

        tabla.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        tabla.getColumnModel().getColumn(0).setMaxWidth(50); //icon
        tabla.getColumnModel().getColumn(0).setPreferredWidth(50);

        tabla.getColumnModel().getColumn(1).setMaxWidth(200); //Tipo mantenimiento
        tabla.getColumnModel().getColumn(1).setPreferredWidth(200);

        tabla.getColumnModel().getColumn(2).setMaxWidth(140); //Equipo
        tabla.getColumnModel().getColumn(2).setPreferredWidth(140);
        
        tabla.getColumnModel().getColumn(3).setMaxWidth(230); //comentario
        tabla.getColumnModel().getColumn(3).setPreferredWidth(230);
        
        tabla.getColumnModel().getColumn(4).setMaxWidth(140); //Usuario
        tabla.getColumnModel().getColumn(4).setPreferredWidth(140);
        
        tabla.getColumnModel().getColumn(5).setMaxWidth(105); //Nombres
        tabla.getColumnModel().getColumn(5).setPreferredWidth(105);
        
        tabla.getColumnModel().getColumn(6).setMaxWidth(150); //Fechas
        tabla.getColumnModel().getColumn(6).setPreferredWidth(150);

        //"Id", "Descripción","Nro. Serie","Modelo","Comentario"
        GUI.alinerColumnaTabla(tabla, 0, GUI.ALINEACION.IZQUIERDA);
        GUI.alinerColumnaTabla(tabla, 1, GUI.ALINEACION.IZQUIERDA);
        GUI.alinerColumnaTabla(tabla, 2, GUI.ALINEACION.IZQUIERDA);
        jScrollPane1.setBorder(BorderFactory.createEmptyBorder());
        tabla.setIntercellSpacing(new Dimension(15, 0));
        //GUI.setFiltroTabla(tabla, txBuscar.getText(), FILTRO_COLS);
    }
    

