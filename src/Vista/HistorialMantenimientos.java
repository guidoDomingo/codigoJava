    private void cboPdfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboPdfActionPerformed
        // TODO add your handling code here:
        String tipo_usuario = cboPdf.getSelectedItem().toString();
        if(tipo_usuario.equals("GENERAL")){
            try {
            Conexion con = new Conexion();
            Connection conn = con.obtenerConexion();
            
            JasperReport reporte = null;
          
            String path = "src/Reportes/report7.jasper";
          
            reporte = (JasperReport) JRLoader.loadObjectFromFile(path);
            
            JasperPrint jprint = JasperFillManager.fillReport(reporte,null, conn);
            
            JasperViewer view = new JasperViewer(jprint, false);
            
            view.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
            
            view.setVisible(true);
            
            
        } catch (JRException ex) {
            Logger.getLogger(HistorialMantenimientos.class.getName()).log(Level.SEVERE, null, ex);
        }
        }else if(tipo_usuario.equals("FILTRADO")){
            try {
            Conexion con = new Conexion();
            Connection conn = con.obtenerConexion();
            
            JasperReport reporte = null;
          
            String path = "src/Reportes/report8.jasper";
            
           
            
            
            Map parametro = new HashMap();
            parametro.put("desde", fechaDesde.getText().toString());
            parametro.put("hasta", fechaHasta.getText().toString());
            
            
            reporte = (JasperReport) JRLoader.loadObjectFromFile(path);
            
            JasperPrint jprint = JasperFillManager.fillReport(reporte,parametro, conn);
            
            JasperViewer view = new JasperViewer(jprint, false);
            
            view.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
            
            view.setVisible(true);
            
            
        } catch (JRException ex) {
            Logger.getLogger(HistorialMantenimientos.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    }//GEN-LAST:event_cboPdfActionPerformed
    
     
